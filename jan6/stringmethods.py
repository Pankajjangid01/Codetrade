#Capitalize
txt= "hello, and Welcome to my World."
print(txt.capitalize()) #convert first character to upper case
print(txt.upper()) #convert string to upper case 
print(txt.casefold()) #convert string to lower case 
print(txt.center(20)) #return a centerd string
print(txt.count('l')) #return th count of a character in a string 
print(txt.encode()) #encode the string 
print(txt.endswith('.')) #return boolean value if the string ends with specific value
print(txt.find("and")) # find the specific value and returns its index  
print(txt.isalnum()) #return true if all character in string are alphanumeric 
print(txt.isalpha()) #return true if all character in string are alphabet
print(txt.isascii()) #return true if all character in string are ascii character
print(txt.isdigit()) #return true if all character in string are in digit
print(txt.istitle())#return true if the string follow the rule of title